public static void main(java.lang.String[] args) {
    java.util.Scanner in = new java.util.Scanner(java.lang.System.in);
    java.lang.System.out.print("Please enter your name: ");
    java.lang.String name = in.nextLine();
    java.lang.System.out.print("Please enter your age: ");
    int age = in.nextInt();
    java.lang.System.out.print("Please enter your mobile number: ");
    java.lang.String m_num = in.nextLine();
    java.lang.System.out.print(("User name: " + name));
    java.lang.System.out.print(("User age: " + age));
    java.lang.System.out.print(("User mobile number: " + m_num));
}