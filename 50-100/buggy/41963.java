private void addCode(final java.util.List<java.lang.String> codeList, final java.lang.String code, final java.lang.String objectName, final java.lang.String field) {
    final java.lang.String formattedCode = formatCode(code, objectName, field);
    if (!(codeList.contains(codeList))) {
        codeList.add(formattedCode);
    }
}