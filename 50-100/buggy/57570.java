private void setupIconColors() {
    ((android.widget.ImageView) (getActivity().findViewById(R.id.ic_category))).setColorFilter(getResources().getColor(R.color.material_gray));
    ((android.widget.ImageView) (getActivity().findViewById(R.id.ic_action_info))).setColorFilter(getResources().getColor(R.color.material_gray));
    ((android.widget.ImageView) (getActivity().findViewById(R.id.ic_header_location))).setColorFilter(getResources().getColor(android.R.color.white));
}