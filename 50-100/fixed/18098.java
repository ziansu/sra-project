private void initOutput() {
    lblOutput = new javax.swing.JLabel("Output");
    txtOutput = new javax.swing.JTextArea();
    lblOutput.setFont(new java.awt.Font("Tahoma", java.awt.Font.BOLD, 16));
    lblOutput.setSize(464, 20);
    txtOutput.setPreferredSize(new java.awt.Dimension(510, 586));
    txtOutput.setFont(new java.awt.Font("Courier New", 0, 12));
    txtOutput.setLineWrap(true);
    txtOutput.setWrapStyleWord(true);
}