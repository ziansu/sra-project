public void setAlarm(java.lang.String yr, java.lang.String mon, java.lang.String dy, java.lang.String hr, java.lang.String min, java.lang.String message) {
    this.year = yr;
    this.month = mon;
    this.day = dy;
    this.hour = hr;
    this.minute = min;
    this.timer = false;
    this.message = message;
}