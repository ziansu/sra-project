public void odmas(java.util.List<java.lang.Character> symbolsList, java.util.List<java.lang.String> numbersList) {
    sqrt(symbolsList, numbersList);
    division(symbolsList, numbersList);
    divisionWithPercentageSymbol(symbolsList, numbersList);
    multiplication(symbolsList, numbersList);
    multiplicationWithStarSymbol(symbolsList, numbersList);
    subtraction(symbolsList, numbersList);
    addition(symbolsList, numbersList);
}