private static void printPrimeNumber(int n, boolean b) {
    if (b) {
        java.lang.System.out.println((n + " is a prime number."));
        java.lang.System.out.println();
    }else {
        java.lang.System.out.println((n + " is not a prime number."));
        java.lang.System.out.println();
    }
}