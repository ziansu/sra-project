public static int getImageForPlayer(java.lang.String playerId) {
    switch (playerId) {
        case "61034" :
            return R.drawable.icon_ralf;
        case "168101" :
            return R.drawable.icon_wulei;
        case "116730" :
            return R.drawable.icon_fernando;
        default :
            return 0;
    }
}